package tugas.tugas2;

/**
 *
 * @author nares
 */
public class matkul {
    String kodeMk, namaMk;
    int sks;
    
    public matkul(String kodeMk, String namaMk, int sks){
        this.kodeMk = kodeMk;
        this.namaMk = namaMk;
        this.sks = sks;
    }
    public String toString(){
        return String.format("%s\t%-40s%s", kodeMk, namaMk, sks);
    }
}